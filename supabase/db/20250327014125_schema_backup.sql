CREATE OR REPLACE FUNCTION "public"."add_covering_id_to_shift_change"("shift_id_param" integer, "covering_profile_id_param" integer) RETURNS "record"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
    shift_rec        RECORD;
    has_conflict     BOOLEAN;
    shift_change_rec RECORD;
BEGIN -- Check for shift conflicts before changing shift status to pending when shift claimed
    shift_rec := get_shift_data(shift_id_param);

    has_conflict := EXISTS(SELECT 1
                           FROM shifts
                           WHERE assigned_user_id = covering_profile_id_param
                             AND slot && shift_rec.slot);
    IF NOT has_conflict THEN
        UPDATE shift_changes
        SET covering_profile_id = covering_profile_id_param,
            status              = 'pending',
            covered_at          = NOW()
        WHERE shift_id = shift_id_param
        RETURNING * INTO shift_change_rec;

        RETURN shift_change_rec;
    ELSE
        RAISE EXCEPTION 'Cannot pick up shift - schedule conflicts with existing shift';
    END IF;
END;
$$;


ALTER FUNCTION "public"."add_covering_id_to_shift_change"("shift_id_param" integer, "covering_profile_id_param" integer) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."add_shift_to_shift_change"("shift_id_param" integer, "coverage_reason_param" "text" DEFAULT 'No reason provided'::"text") RETURNS "record"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
    shift_rec        RECORD;
    shift_change_rec RECORD;
BEGIN
    shift_rec := get_shift_data(shift_id_param);
    -- Update needs coverage in shift table
    UPDATE shifts
    SET needs_coverage = TRUE
    WHERE shift_id = shift_id_param;

    -- Insert shift, then return shift change record
    WITH inserted_shift_change AS (
        INSERT INTO shift_changes (shift_id, og_shift_profile_id, coverage_reason)
            VALUES (shift_rec.shift_id, shift_rec.assigned_user_id, coverage_reason_param)
            RETURNING *)
    SELECT *
    FROM inserted_shift_change
    INTO shift_change_rec;

    RETURN shift_change_rec;
EXCEPTION
    WHEN OTHERS THEN
        RAISE;
END;
$$;


ALTER FUNCTION "public"."add_shift_to_shift_change"("shift_id_param" integer, "coverage_reason_param" "text") OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."add_supervisor"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
BEGIN -- If user not supervisor, add to table w/ new id
    IF
        NEW.role = 'supervisor' AND (OLD.role IS NULL OR OLD.role != 'supervisor') THEN
        INSERT INTO supervisors(supervisor_id)
        VALUES (NEW.profile_int_id)
        ON CONFLICT (supervisor_id) DO NOTHING;
    ELSIF -- If user loses supervisor role, delete from table
        OLD.role = 'supervisor' AND NEW.role != 'supervisor' THEN
        DELETE
        FROM supervisors
        WHERE supervisor_id = NEW.profile_int_id;
    END IF;
    RETURN NEW;
END;
$$;


ALTER FUNCTION "public"."add_supervisor"() OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."approve_update_shift_w_profile_ids"("shift_id_param" integer, "supervisor_id_param" integer) RETURNS "record"
    LANGUAGE "plpgsql"
    AS $$
DECLARE
    shift_change_rec RECORD;
    is_supervisor    BOOLEAN;
BEGIN -- Check supervisor privileges, update shift, update needs_coverage to false
    SELECT *
    INTO shift_change_rec
    FROM shift_changes
    WHERE shift_id = shift_id_param;

    -- Handle missing record data
    IF NOT FOUND THEN
        RAISE EXCEPTION 'Shift w/ ID % not found', shift_id_param;
    ELSE
        IF shift_change_rec.covering_profile_id IS NULL THEN
            RAISE EXCEPTION 'Shift w/ ID % missing covering profile id', shift_id_param;
        END IF;

        is_supervisor := is_supervisor(supervisor_id_param);
        IF
            is_supervisor THEN
            UPDATE shift_changes
            SET approved_by_supervisor_id = supervisor_id_param,
                status                    = 'approved'
            WHERE shift_id = shift_id_param;

            UPDATE shifts
            SET assigned_user_id = shift_change_rec.covering_profile_id,
                needs_coverage   = FALSE
            WHERE shift_id = shift_id_param
            RETURNING * INTO shift_change_rec;
            RETURN shift_change_rec;
        ELSE
            RAISE EXCEPTION 'Error updating shift change in shift_change_id %',
                shift_change_rec.shift_change_id;
        END IF;
    END IF;
END;
$$;


ALTER FUNCTION "public"."approve_update_shift_w_profile_ids"("shift_id_param" integer, "supervisor_id_param" integer) OWNER TO "postgres";


